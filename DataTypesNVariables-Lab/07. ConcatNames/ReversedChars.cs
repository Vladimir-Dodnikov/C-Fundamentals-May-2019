using System;

namespace _06._ReversedChars
{
    class ReversedChars
    {
        static void Main()
        {
            char typeOne = char.Parse(Console.ReadLine());
            char typeTwo = char.Parse(Console.ReadLine());
            char typeThree = char.Parse(Console.ReadLine());

            Console.WriteLine($"{typeThree} {typeTwo} {typeOne}");
        }
    }
}
